## ddd_simulation.R
## ----------------------------------------------------------------------
## Triple-diff simulation: shows DiD biased by differential state-level
## trends, placebo DiD also biased the SAME WAY, and DDD recovers ATT.
##
## DGP (port of CI-2/Lab/DDD/ddd2.R by Cunningham & Butts, 2024):
##   - 40 states × 50 cities × 3 worker groups × 11 years (2010–2020)
##   - States 1–20 are "experimental" (mandate states), 21–40 are control
##   - Worker 1 = single men;  Worker 2 = married women 20-40;  Worker 3 = older women
##   - Treatment: starting 2015, mandate states' married women 20-40 take a
##     wage hit of $5,000 (the ATT we want to recover)
##   - Differential trend: mandate states have a different state trend
##     than control states -- this is what breaks the naive 2x2 DiD
##
## Output:
##   - console: DiD-biased ≈ -$7,487
##              Placebo DiD ≈ -$2,507  (the bias itself)
##              DDD ≈ -$4,972  (close to the true ATT of -$5,000)
##   - figures/triple_diff_trends.pdf
##   - figures/triple_diff_eventstudy.pdf
##   - tables/triple_diff_cells.tex (8-cell table for the deck)
## ----------------------------------------------------------------------

suppressPackageStartupMessages({
  library(dplyr)
  library(tidyr)
  library(ggplot2)
  library(fixest)
  library(broom)
})

source(file.path("..", "..", "..", "..", "_themes", "remix_theme.R"))

set.seed(20200403)

## ---- DGP ----------------------------------------------------------------
df <- expand_grid(
  state  = 1:40,
  city   = 1:50,
  worker = 1:3,                    # 1 = men, 2 = married women, 3 = older women
  year   = 2010:2020
)

df$experimental <- as.numeric(df$state <= 20)
df$after        <- as.numeric(df$year >= 2015)

df$city <- fixest::to_integer(df$state, df$city)
df$id   <- fixest::to_integer(df$state, df$city, df$worker)

# Baseline earnings differ across worker groups
df$baseline <- case_when(
  df$worker == 1 ~ 50000,
  df$worker == 2 ~ 45000,
  df$worker == 3 ~ 40000
)
# Mandate states start 50% higher (just a level shift -- irrelevant for DiD)
df$baseline <- if_else(df$experimental == 1, df$baseline * 1.5, df$baseline)

df$year_diff <- df$year - 2010

# *** THE NON-PARALLEL TREND ***
# Mandate states trend at $1,000/yr, control states at $1,500/yr.
# Married women have an extra $500/yr group trend (vs $1,000 for men/older women).
# Together these create a state-level differential trend that biases the simple DiD.
df$state_trend <- if_else(df$experimental == 1, 1000, 1500)
df$group_trend <- if_else(df$worker == 2,       500, 1000)

df$error <- rnorm(nrow(df), mean = 0, sd = 1500)
df$y0    <- df$baseline + df$state_trend * df$year_diff +
                          df$group_trend * df$year_diff + df$error

# Treatment: -$5,000 ATT for married women in mandate states post-2015
df$treat    <- (df$experimental == 1 & df$worker == 2 & df$after == 1)
df$y1       <- df$y0 - 5000 * df$treat
df$earnings <- df$treat * df$y1 + (1 - df$treat) * df$y0

## ---- 8 cells ------------------------------------------------------------
cell <- function(after_v, exp_v, married_v) {
  with(df, mean(earnings[after == after_v & experimental == exp_v &
                         (worker == 2) == married_v]))
}

# married women cells
ame <- cell(1, 1, TRUE);  bme <- cell(0, 1, TRUE)
amn <- cell(1, 0, TRUE);  bmn <- cell(0, 0, TRUE)
# placebo (single men + older women) cells
asoe <- cell(1, 1, FALSE); bsoe <- cell(0, 1, FALSE)
ason <- cell(1, 0, FALSE); bson <- cell(0, 0, FALSE)

## ---- Three estimators ---------------------------------------------------
DiD_naive   <- (ame  - bme)  - (amn  - bmn)
DiD_placebo <- (asoe - bsoe) - (ason - bson)
DDD         <- DiD_naive - DiD_placebo

cat("------- THREE ESTIMATORS -------\n")
cat(sprintf("True ATT (built into DGP):  -5,000\n"))
cat(sprintf("Naive DiD  (married only):  %+.0f  <-- biased\n", DiD_naive))
cat(sprintf("Placebo DiD (men + older):  %+.0f  <-- the bias itself\n", DiD_placebo))
cat(sprintf("Triple Diff (DiD - Plac):   %+.0f  <-- recovers the ATT\n", DDD))

## ---- 8-cell LaTeX table for the deck -----------------------------------
tbl_lines <- c(
