## ---- Trends figure ------------------------------------------------------
# Average earnings by state-type × group × year for the plot
trends <- df %>%
  mutate(
    group   = case_when(worker == 2 ~ "Married women 20-40",
                        TRUE        ~ "Placebo: men + older women"),
    state_type = if_else(experimental == 1, "Mandate states",  "Control states")
  ) %>%
  group_by(year, group, state_type) %>%
  summarise(earnings = mean(earnings), .groups = "drop")

p_trends <- ggplot(trends, aes(x = year, y = earnings,
                               color = state_type, linetype = group)) +
  geom_vline(xintercept = 2014.5,
             color = remix_color("warmgray"), linewidth = 0.5,
             linetype = "dotted") +
  annotate("text", x = 2014.6, y = 95000,
           label = "mandate begins",
           color = remix_color("warmgray"),
           size = 3.0, fontface = "italic", hjust = 0) +
  geom_line(linewidth = 0.9) +
  geom_point(size = 1.6) +
  scale_color_manual(values = c("Mandate states" = remix_color("forest"),
                                "Control states" = remix_color("ocean")),
                     name = NULL) +
  scale_linetype_manual(values = c("Married women 20-40" = "solid",
                                   "Placebo: men + older women" = "longdash"),
                        name = NULL) +
  scale_x_continuous(breaks = seq(2010, 2020, 2)) +
  scale_y_continuous(labels = function(x) paste0("$", format(x/1000, big.mark=","), "k")) +
  labs(
    title    = "Two diff-in-diffs. One bias. Both groups feel the same differential trend.",
    subtitle = "Treated cells (married women × mandate states) drop $5,000 in 2015. The placebo group does NOT -- but its DiD is still biased.",
    x = NULL, y = "Mean earnings",
    caption = "Simulated DGP. Mandate states trend $500/yr SLOWER than control states. That trend bias hits BOTH groups equally -- the basis for DDD."
  ) +
  theme_remix() +
  theme(legend.position = "bottom",
        legend.box = "vertical",
        legend.spacing.y = unit(-3, "pt"))

remix_save("../../figures/triple_diff_trends.pdf", p_trends,
