## ---- Event-study coefplot ----------------------------------------------
df$treated1 <- case_when(
  df$experimental == 1 & df$worker == 2 ~ 1,
  df$worker == 2 ~ 0,
  .default = NA_integer_
)
df$treated_placebo <- case_when(
  df$experimental == 1 & df$worker %in% c(1, 3) ~ 1,
  df$worker %in% c(1, 3) ~ 0,
  .default = NA_integer_
)
df$married_women <- as.numeric(df$worker == 2)

df$treated1_x_year <- if_else(df$treated1 == 1, df$year * df$treated1, 2014)
es_treated1 <- feols(
  earnings ~ i(treated1_x_year, ref = 2014) | year + treated1,
  df, cluster = ~state
)

df$placebo_x_year <- if_else(df$treated_placebo == 1, df$year * df$treated_placebo, 2014)
es_placebo <- feols(
  earnings ~ i(placebo_x_year, ref = 2014) | year + treated_placebo,
  df, cluster = ~state
)

es_triple <- feols(
  earnings ~ i(year, experimental * married_women) +
    i(year) + i(experimental) + i(married_women) +
    i(year, experimental) + i(year, married_women) +
    i(experimental * married_women),
  data = df, cluster = ~state
)

pts_treated1 <- es_treated1 |> broom::tidy() |>
  mutate(estimator = "Naive DiD (biased)",
         year = as.numeric(sub("treated1_x_year::", "", term)))
pts_placebo  <- es_placebo  |> broom::tidy() |>
  mutate(estimator = "Placebo DiD (also biased)",
         year = as.numeric(sub("placebo_x_year::", "", term)))
pts_triple   <- es_triple   |> broom::tidy() |>
  filter(grepl("year::.*:experimental \\* married_women", term)) |>
  mutate(estimator = "Triple Diff (unbiased)",
         year = as.numeric(sub("year::(.*):experimental.*", "\\1", term)))

pts <- bind_rows(pts_treated1, pts_placebo, pts_triple) |>
  filter(!is.na(year))
pts$estimator <- factor(pts$estimator,
                        levels = c("Naive DiD (biased)",
                                   "Placebo DiD (also biased)",
                                   "Triple Diff (unbiased)"))

p_es <- ggplot(pts, aes(x = year, y = estimate, color = estimator)) +
  geom_hline(yintercept = 0, color = remix_color("charcoal"),
             linewidth = 0.4, linetype = "dashed") +
  geom_hline(yintercept = -5000, color = remix_color("warmgray"),
             linewidth = 0.4, linetype = "dotted") +
  annotate("text", x = 2010.1, y = -5000,
           label = "true ATT = -$5,000",
           color = remix_color("warmgray"),
           size = 2.7, fontface = "italic", hjust = 0, vjust = -0.5) +
  geom_vline(xintercept = 2014.5, color = remix_color("warmgray"),
             linewidth = 0.4, linetype = "dotted") +
  geom_errorbar(aes(ymin = estimate - 1.96*std.error,
                    ymax = estimate + 1.96*std.error),
                width = 0.18, linewidth = 0.5) +
  geom_point(size = 2.3) +
  scale_color_manual(values = c(
    "Naive DiD (biased)"        = remix_color("ocean"),
    "Placebo DiD (also biased)" = remix_color("warmgray"),
    "Triple Diff (unbiased)"    = remix_color("forest")
  ), name = NULL) +
  scale_x_continuous(breaks = 2010:2020) +
  scale_y_continuous(labels = function(x) paste0("$", format(x, big.mark=","))) +
  labs(
    title    = "Two biased DiDs, one unbiased Triple Diff",
    subtitle = "The bias of the naive DiD equals the placebo DiD. Subtract them -- get the ATT.",
    x = NULL, y = "Event-study coefficient",
    caption = "95% CI; SE clustered at state. The two biased DiDs track each other above zero; the DDD lands near -$5,000 after treatment."
  ) +
  theme_remix() +
  theme(legend.position = "bottom")

remix_save("../../figures/triple_diff_eventstudy.pdf", p_es,
           width = 9.4, height = 5.2)
cat("Saved: ../../figures/triple_diff_eventstudy.pdf\n")
